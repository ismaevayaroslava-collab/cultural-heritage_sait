// ЭЛЕМЕНТЫ DOM
const markers = document.querySelectorAll('.marker');
const modal = document.getElementById('infoModal');
const modalTitle = document.getElementById('modalTitle');
const modalBody = document.getElementById('modalBody');
const closeBtn = document.querySelector('.close');
const peopleFilter = document.getElementById('people-filter');
const regionFilter = document.getElementById('region-filter');

// ОБРАБОТЧИКИ СОБЫТИЙ

// Клик по маркеру
markers.forEach(marker => {
    marker.addEventListener('click', function () {
        const peopleKey = this.getAttribute('data-people');
        const data = peoplesData[peopleKey];

        if (data) {
            modalTitle.textContent = data.title;
            modalBody.innerHTML = data.text;
            modal.style.display = 'flex';
            document.body.style.overflow = 'hidden';
        }
    });
});

// Закрытие модального окна
closeBtn.addEventListener('click', () => {
    modal.style.display = 'none';
    document.body.style.overflow = 'auto';
});

// Закрытие при клике вне окна
window.addEventListener('click', (e) => {
    if (e.target === modal) {
        modal.style.display = 'none';
        document.body.style.overflow = 'auto';
    }
});

// Закрытие
document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape' && modal.style.display === 'flex') {
        modal.style.display = 'none';
        document.body.style.overflow = 'auto';
    }
});

// ФИЛЬТРЫ

// Фильтр по народу
peopleFilter.addEventListener('change', function () {
    const selectedPeople = this.value;

    markers.forEach(marker => {
        if (selectedPeople === 'all' || marker.getAttribute('data-people') === selectedPeople) {
            marker.style.opacity = '1';
            marker.style.pointerEvents = 'auto';
            marker.style.transform = 'scale(1)';
        } else {
            marker.style.opacity = '0.2';
            marker.style.pointerEvents = 'none';
            marker.style.transform = 'scale(0.8)';
        }
    });
});

// Фильтр по региону
regionFilter.addEventListener('change', function () {
    const selectedRegion = this.value;

    markers.forEach(marker => {
        // Получаем все регионы для маркера (может быть несколько через пробел)
        const regions = marker.getAttribute('data-regions');

        if (selectedRegion === 'all' || regions.includes(selectedRegion)) {
            marker.style.opacity = '1';
            marker.style.pointerEvents = 'auto';
            marker.style.transform = 'scale(1)';
        } else {
            marker.style.opacity = '0.2';
            marker.style.pointerEvents = 'none';
            marker.style.transform = 'scale(0.8)';
        }
    });
});

// АНИМАЦИЯ ПРИ ЗАГРУЗКЕ
window.addEventListener('load', () => {
    markers.forEach((marker, index) => {
        marker.style.opacity = '0';
        marker.style.transform = 'translateY(20px)';

        setTimeout(() => {
            marker.style.transition = 'all 0.6s ease';
            marker.style.opacity = '1';
            marker.style.transform = 'translateY(0)';
        }, index * 100);
    });
});

// ПЕРЕКЛЮЧЕНИЕ ТЕМЫ
const themeToggle = document.getElementById('themeToggle');
const themeIcon = document.querySelector('.theme-icon');

// Проверяем сохранённую тему при загрузке
const savedTheme = localStorage.getItem('theme');
if (savedTheme === 'light') {
    document.body.classList.add('light-theme');
    themeIcon.textContent = '🌙';
}

// Переключение темы
themeToggle.addEventListener('click', () => {
    document.body.classList.toggle('light-theme');

    // Меняем иконку и сохраняем тему
    if (document.body.classList.contains('light-theme')) {
        themeIcon.textContent = '🌙';
        localStorage.setItem('theme', 'light');
    } else {
        themeIcon.textContent = '☀️';
        localStorage.setItem('theme', 'dark');
    }
});